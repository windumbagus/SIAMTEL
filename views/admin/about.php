		<h2 class="thead">Tentang ISC Telkom</h2>
			<p class="padding">
				Divisi Information System Center adalah salah satu Divisi yang diperankan untuk penyelenggaraan aktivitas bisnis dengan fokus pada pengelolaan IT Service berikut pengelolaan ICT Solution, sehingga beberapa aktivitas operasi bisnis dari Unit lain yang penyelenggaranya diintegrasikan dan dikelompokkan ke dalam organisasi ini :
				<ul>
					<li> Unit Service Delivery dan Unit Alliance & Solution dari Divisi Enterprise Service</li>

					<li> Fungsi Business Solution Development, fungsi product & partnership Management dan fungsi Project Management dari Divisi Business Service.</li>

					<li> Unit Service Platform Management dari Divisi Solution Convergent.</li>
				</ul>
				<p class="padding"> ISC berawal dari proyek Mekanisasi Administrasi Telekomunikasi (MEKADTEL) pada tahun 1977 yang bertujuan untuk melakukan mekanisasi terhadap sistem billing.
