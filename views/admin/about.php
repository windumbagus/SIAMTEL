		<h2 class="thead">Layanan ISC Telkom</h2>
		<p class="padding"> 
			ISC melayani dan menunjang karyawan Telkom agar mereka dapat bekerja seoptimal mungkin melalui layanan Informasi seperti:
		<ul>
			<li>Seat Management, layanan yang menyediakan perangkat-perangkat kerja karyawan seperti PC, Laptop, Projector, Printer, Scanner, dsb. Selain itu divisi ini juga melayani service kerusakan maupun trouble yang dialami perangkat karyawan tersebut.</li><br>

			<li>Networking, layanan yang memberikan akses internet bagi karywan Telkom dan melaukan maintenance pada jaringan perusahaan Telkom.</li><br>

			<li>Website Telkom, layanan ini mengolah web menu kerja karyawan Telkom seperti portal.telkom.co.id, notadinas.telkom.co.id, mail.telkom.co.id, pwb.telkom.co.id, sppd.telkom.co.id, dll.
			Mail Telkom, layanan yang mengatur dan memonitoring kinerja server mail telkom (pop3.telkom.co.id dan smtp.telkom.co.id), serta memberi kuota disk mail untuk karywan Telkom.</li>
		</ul>
